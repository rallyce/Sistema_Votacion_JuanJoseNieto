    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        String name, usuario, password, mesa_string;

        int validacion = 0, mesa_int;

        name = jTextField_name.getText().trim();
        usuario = jTextField_usuario.getText().trim();
        password = jTextField_pass.getText().trim();

        mesa_string = jTextField_mesa.getText().trim();

        if (name.equals("")) {

            validacion++;

        }

        if (usuario.equals("")) {

            validacion++;

        }

        if (password.equals("")) {

            validacion++;

        }

        if (mesa_string.equals("")) {

            validacion++;

        }

        try {

            if (validacion == 0) {

                Connection cn_2 = get_connection.connect();

                PreparedStatement pst_2 = cn_2.prepareStatement("insert into usuarios values(?,?,?,?,?,?)");

                mesa_int = Integer.parseInt(mesa_string);

                pst_2.setInt(1, 0);
                pst_2.setString(2, name);
                pst_2.setString(3, usuario);
                pst_2.setString(4, password);
                pst_2.setString(5, "Estudiante");
                pst_2.setInt(6, mesa_int);

                pst_2.executeUpdate();
                cn_2.close();

                JOptionPane.showMessageDialog(null, "Estudiante registrado con exito!");

            } else {

                JOptionPane.showMessageDialog(
                        null, "Llene todo los campos requeridos.", "ERROR, FALTA DE DATOS!", JOptionPane.ERROR_MESSAGE);

            }

        } catch (SQLException e) {

            System.err.println("SQL ERROR! " + e);
            JOptionPane.showMessageDialog(
                    null, "Error con la conexion a la base de datos, contacte al programador", "ERROR BASE DE DATOS!", JOptionPane.ERROR_MESSAGE);

        }

        try {

            if (validacion == 0) {

                Connection cn_3 = get_connection.connect();

                PreparedStatement pst_3 = cn_3.prepareStatement("insert into est_no_voto values(?,?,?,?)");

                mesa_int = Integer.parseInt(mesa_string);
                
                pst_3.setInt(1, 0);
                pst_3.setString(2, name);
                pst_3.setString(3, "Estudiante");
                pst_3.setInt(4, mesa_int);
                
                pst_3.executeUpdate();
                
                cn_3.close();
            }
            
        } catch (NumberFormatException | SQLException e) {
            
            System.err.println("SQL ERROR! " + e);
            JOptionPane.showMessageDialog(
                    null, "Error con la conexion a la base de datos, contacte al programador", "ERROR BASE DE DATOS!", JOptionPane.ERROR_MESSAGE);

        }

    }//GEN-LAST:event_jButton2ActionPerformed

